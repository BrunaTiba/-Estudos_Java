package Fundamentos;

public class peradoresAtribuicao {

	
	public static void main(String[] args) {
		int a = 7;
		int b = 3;
		
		b+=a;
		//+= informa que o valor de a esta sendo somando ao b
		// b = b+a
		
		b*=4;
		//b = b*4		
		
		b/=2;
		// b = b /2
		
		b-=2; 
		//b = b- 2
		
		b%=2;
		// resto de b divido por 2
		
		
		System.out.println(b);
		
		
		
	}
}
